package day03_EscapeSequences;

public class Println_VS_Print {

    public static void main(String[] args) { // main method

        System.out.println();
        System.out.println("Hello Cydeo");
        System.out.println("How are you today?");

        System.out.println("--------------------");


        System.out.print("Hello Cydeo");
        System.out.print("How are you today?");
        System.out.println("hiya");
        System.out.println("How are you today?");

    }




}

/*
Topics:
    println method (goes to new line)
    print method (doesnt go to new line)
    comments use "//" for single line  use "/*" for multi line comments
 */